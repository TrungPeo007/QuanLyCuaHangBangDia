    private void tblThongTinKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblThongTinKHMouseClicked
        int row=tblThongTinKH.getSelectedRow();
        if(row>-1)
        {
        txtMaKH.setEnabled(false);
        btnXoa.setEnabled(true);
        btnSua.setEnabled(true);
        btnLuu.setEnabled(false);
        txtMaKH.setText(String.valueOf(model.getValueAt(tblThongTinKH.getSelectedRow(),0)));
        txtHoTenKH.setText(String.valueOf(model.getValueAt(tblThongTinKH.getSelectedRow(),1)));
        txtCMND.setText(String.valueOf(model.getValueAt(tblThongTinKH.getSelectedRow(),2)));
        String gt=tblThongTinKH.getValueAt(row, 3).toString();
        if(gt.equalsIgnoreCase("Nam"))
        {
            radNam.setSelected(true);
            radNu.setSelected(false);
        }  else
        {
            radNu.setSelected(true);
            radNam.setSelected(false);
        }
        
        txtSDT.setText(String.valueOf(model.getValueAt(tblThongTinKH.getSelectedRow(),4)));
        txtDiaChi.setText(String.valueOf(model.getValueAt(tblThongTinKH.getSelectedRow(),5)));
        }
        else
             JOptionPane.showMessageDialog(rootPane,"Vui lòng click vào 1 dòng!","Thông báo",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_tblThongTinKHMouseClicked

