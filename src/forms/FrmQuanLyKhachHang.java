    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        if(txtHoTenKH.getText().equals(""))
       {
           JOptionPane.showMessageDialog(this, "Vui lòng nhập họ tên khách hàng! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtHoTenKH.requestFocus();
       }
        else if(txtCMND.getText().equals(""))
       {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập CMND! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtCMND.requestFocus();   
       }
       else if(txtCMND.getText().length()<9|txtCMND.getText().length()>12|txtCMND.getText().length()==10|txtCMND.getText().length()==11)
       {
            JOptionPane.showMessageDialog(this, "CMND phải 9 số hoặc 12 số! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtCMND.requestFocus();   
       }
       else if(txtSDT.getText().equals(""))
       {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtSDT.requestFocus();     
       }
       else if(txtSDT.getText().length()<10|txtSDT.getText().length()>11)
       {
        JOptionPane.showMessageDialog(this, "Số điện thoại phải 10 số hoặc 11 số! ","Thông báo",JOptionPane.WARNING_MESSAGE);
        txtSDT.requestFocus(); 
        }
      
       else if(txtDiaChi.getText().equals(""))
       {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtDiaChi.requestFocus();     
       }
        else
       {
        String MaKH=txtMaKH.getText();
     String hoten=txtHoTenKH.getText();
     String cmnd=txtCMND.getText();
       String gt=radNam.isSelected()?"Nam":"Nữ";
       String SDT=txtSDT.getText();
       String DChi=txtDiaChi.getText();
       KhachHang s=new KhachHang(MaKH,hoten,cmnd,gt,SDT,DChi);
     int tb=JOptionPane.showConfirmDialog(this, "Bạn muốn sửa lại khách hàng "+txtHoTenKH.getText()+ " này chứ ?","Thông báo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(tb==JOptionPane.YES_OPTION)
        {
            JOptionPane.showMessageDialog(rootPane,"Đã sửa thành công");
           KhachHangBL.SuaKhachHang(s);
        model =(DefaultTableModel)tblThongTinKH.getModel();
        model.setRowCount(0);
        initKhachHang();
         ResetAll();
        }   
       }

    }//GEN-LAST:event_btnSuaActionPerformed

