    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if(txtTenPhim.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên phim! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtTenPhim.requestFocus();
        }
        else if(txtDienVien.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên diễn viên! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtDienVien.requestFocus();
        }
        else if(txtDaoDien.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đạo diễn! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtDaoDien.requestFocus();
        }
        else
        {
            String ten=txtTenPhim.getText();
            String DienVien=txtDienVien.getText();
            String DaoDien=txtDaoDien.getText();
            String MaBD=txtMaBD2.getText();
            ThongTinPhim s=new ThongTinPhim(ten,DienVien,DaoDien,MaBD);
            int smt= ThongTinPhimBL.themPhim(s);
            if(smt>0)
            {

                JOptionPane.showMessageDialog(rootPane, "Lưu thông tin bài hát thành công");

                model =(DefaultTableModel)tblPhim.getModel();
                model.setRowCount(0);
                initthongtinp();
                ResetAll();
            }

        }

    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        int ID=Integer.parseInt(id);
        int tb=JOptionPane.showConfirmDialog(this,"Bạn có muốn xóa dữ liệu này không?","Thông báo",JOptionPane.YES_NO_OPTION);
        if(tb==JOptionPane.YES_OPTION)
        {
            JOptionPane.showMessageDialog(rootPane,"Đã xóa thành công");
            ThongTinPhimBL.XoaPhim(ID);
            model =(DefaultTableModel)tblPhim.getModel();
            model.setRowCount(0);
            initthongtinp();
            ResetAll();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if(txtTenPhim.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên bài hát! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtTenPhim.requestFocus();
        }
        else if(txtDienVien.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên ca sĩ! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtDienVien.requestFocus();
        }
        else if(txtDaoDien.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhạc sĩ! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtDaoDien.requestFocus();
        }
        else
        {
             int tb=JOptionPane.showConfirmDialog(this, "Bạn muốn sửa lại dữ liệu này chứ ?","Thông báo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(tb==JOptionPane.YES_OPTION)
        {
            String MaBD=txtMaBD2.getText();
            String Ten=txtTenPhim.getText();
            String Casi=txtDienVien.getText();
            String NhacSi=txtDaoDien.getText();
            int ID=Integer.parseInt(id);
           ThongTinPhimBL.SuaPhim(ID,Ten,Casi,NhacSi,MaBD);
            JOptionPane.showMessageDialog(rootPane,"Cập nhật thành công");
            model =(DefaultTableModel)tblPhim.getModel();
            model.setRowCount(0);
            initthongtinp();
        }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

