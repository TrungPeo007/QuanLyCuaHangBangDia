    private void tblPhimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhimMouseClicked
        int row=tblPhim.getSelectedRow();
        if(row>-1)
        {
        btnXoa.setEnabled(true);
        btnSua.setEnabled(true);
        btnLuu.setEnabled(false);
        id=String.valueOf(model.getValueAt(tblPhim.getSelectedRow(),0));
        txtTenPhim.setText(String.valueOf(model.getValueAt(tblPhim.getSelectedRow(),1)));
        txtDienVien.setText(String.valueOf(model.getValueAt(tblPhim.getSelectedRow(),2)));
        txtDaoDien.setText(String.valueOf(model.getValueAt(tblPhim.getSelectedRow(),3)));
        txtMaBD2.setText(String.valueOf(model.getValueAt(tblPhim.getSelectedRow(),4)));
    }
        else
         JOptionPane.showMessageDialog(rootPane,"Vui lòng click vào 1 dòng!","Thông báo",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_tblPhimMouseClicked

