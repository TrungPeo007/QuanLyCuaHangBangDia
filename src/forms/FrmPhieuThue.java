    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
     String NgayTh=txtNgayThue.getText();
        
      
        
       if(cboMaNV.getSelectedItem().equals("------Chọn--------"))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn mã nhân viên !","Thông báo",JOptionPane.WARNING_MESSAGE);
            cboMaNV.requestFocus();   
        }
         else if(txtNgayThue.getText().equals(""))
        {
           JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày thuê! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtNgayThue.requestFocus();
        }
        else if(!NgayTh.matches("^\\d{4}\\-(0[1-9]|1[012])\\-(0[1-9]|[12][0-9]|3[01])$"))
        {
           JOptionPane.showMessageDialog(rootPane,"'"+txtNgayThue.getText()+"'"+" không đúng định định dạng ngày thuê.\nPhải nhập đúng định dạng yyyy-MM-dd","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtNgayThue.requestFocus();  
        }
        else if(cboMaKH.getSelectedItem().equals("------Chọn--------"))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn mã khách hàng !","Thông báo",JOptionPane.WARNING_MESSAGE);
            cboMaKH.requestFocus();   
        }
        else if(txtThoiHan.getText().equals(""))
        {
           JOptionPane.showMessageDialog(this, "Vui lòng nhập thời trả! ","Thông báo",JOptionPane.WARNING_MESSAGE);
            txtThoiHan.requestFocus();
        }
       
        else
        {
        String MaPT=txtMaPT.getText();
        String MaNV=cboMaNV.getSelectedItem().toString();
        Date NgayThue=Date.valueOf(txtNgayThue.getText());
        String MaKH=cboMaKH.getSelectedItem().toString();
        int ThoiHan=Integer.parseInt(txtThoiHan.getText());
        double TongTien=Double.parseDouble(txtTongTien.getText());
         PhieuThueBL.SuaPT(MaPT,MaNV,NgayThue,MaKH,ThoiHan);
      int tb=JOptionPane.showConfirmDialog(this, "Bạn muốn sửa lại phiếu thuê "+txtMaPT.getText()+" chứ ?","Thông báo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(tb==JOptionPane.YES_OPTION)
        {
        JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
        
        model =(DefaultTableModel)tblPT.getModel();
        model.setRowCount(0);
        initPhieuThue();
        ResetAll();
        }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

