    private void btnCTPTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCTPTActionPerformed
        int row=tblPT.getSelectedRow();
        if(row>-1)
        {       
        List<ChiTietPhieuThue>listCTPT;
       String ma=txtMaPT.getText();
       listCTPT=ChiTietPhieuThueBL.dsCTPT(ma);
       FrmChiTietPhieuThue ctpt=new FrmChiTietPhieuThue();
       ctpt.txtMaPT1.setText(txtMaPT.getText());
       ctpt.txtThoiHan.setText(txtThoiHan.getText());
       ctpt.txtTongTien.setText(txtTongTien.getText());
       DefaultTableModel model2=(DefaultTableModel)ctpt.tblThongTin.getModel();
         
            for(ChiTietPhieuThue ct:listCTPT)
            {
               
                model2.addRow(new Object[]{ct.getStrMaBD(),ct.getStrTenBD(),ct.getIntSL(),ct.getDbDonGia(),ct.getFltGiamGia(),ct.getDbThanhTien()});
               
            }
        ctpt.setVisible(true);
      this.dispose();  
        }
        else
            JOptionPane.showMessageDialog(rootPane,"Vui lòng click vào 1 dòng trước khi xem chi tiết phiếu thuê!","Thông báo",JOptionPane.WARNING_MESSAGE);
            
    }//GEN-LAST:event_btnCTPTActionPerformed

    private void tblPTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPTMouseClicked

        int row=tblPT.getSelectedRow();
        if(row>-1)
        {
        btnLuu.setEnabled(false);
        txtMaPT.setEnabled(false);
        btnXoa.setEnabled(true);
        btnSua.setEnabled(true);
        txtMaPT.setText(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),0)));
        cboMaNV.setSelectedItem(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),1)));
        txtNgayThue.setText(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),2)));
        cboMaKH.setSelectedItem(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),3)));
       txtThoiHan.setText(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),4)));
       txtTongTien.setText(String.valueOf(model.getValueAt(tblPT.getSelectedRow(),5)));
        }
       else
          JOptionPane.showMessageDialog(rootPane,"Vui lòng click vào 1 dòng!","Thông báo",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_tblPTMouseClicked

